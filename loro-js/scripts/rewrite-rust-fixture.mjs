function normalizeRuntimeJson(value) {
  if (value instanceof Uint8Array) return [...value];
  if (Array.isArray(value)) return value.map(normalizeRuntimeJson);
  if (value == null || typeof value !== "object") return value;
  const output = {};
  for (const [key, child] of Object.entries(value)) {
    output[key === "fractionalIndex" ? "fractional_index" : key] =
      normalizeRuntimeJson(child);
  }
  if ("fractional_index" in output && output.parent === undefined) output.parent = null;
  return output;
}
