const fixtureUrl = (name) => new URL(`../tests/fixtures/rust/${name}`, import.meta.url);
const fixture = (name) => new Uint8Array(readFileSync(fixtureUrl(name)));

const rewrittenBlocks = decodeFastUpdates(fixture("updates.blob")).map((block) =>
  encodeChangeBlock(decodeChangeBlock(block)),
);
writeFileSync(fixtureUrl("updates.ts.blob"), encodeFastUpdates(rewrittenBlocks));

const snapshot = decodeFastSnapshot(fixture("snapshot.blob"));
const history = decodeSstable(snapshot.oplog).map((entry) => ({
  key: entry.key,
  value:
    entry.key.length === 12
      ? encodeChangeBlock(decodeChangeBlock(entry.value))
      : entry.value,
}));
const rewrittenSnapshot = encodeFastSnapshot({
  oplog: encodeSstable(history, { compression: "none" }),
  state: encodeStateSnapshotStore(decodeStateSnapshotStore(snapshot.state), {
    compression: "none",
  }),
  shallowRootState: encodeStateSnapshotStore(
    decodeStateSnapshotStore(snapshot.shallowRootState),
    { compression: "none" },
  ),
});
writeFileSync(fixtureUrl("snapshot.ts.blob"), rewrittenSnapshot);

const checkedUpdates = decodeFastUpdates(fixture("updates.ts.blob"));
if (checkedUpdates.length !== rewrittenBlocks.length) {
  throw new Error("rewritten updates block count mismatch");
}
for (let index = 0; index < checkedUpdates.length; index += 1) {
  if (!bytesEqual(checkedUpdates[index], rewrittenBlocks[index])) {
    throw new Error(`rewritten updates block ${index} mismatch`);
  }
}

